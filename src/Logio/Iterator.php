<?php

namespace Logio;

use MVar\LogParser\LogIterator;

class Iterator extends LogIterator
{
    private $name;

    /**
     * @param string $name
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
